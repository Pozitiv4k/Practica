    {
        $post = new Post;
        $post->name = $request->name;
        $post->email = $request->email;
        $post->tema = $request->tema;
        $post->description = $request->description;
        $post->save();
        return redirect('contact')->with('status', 'Mesajul dvs a fost trimis');
    }

